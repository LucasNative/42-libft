#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	len;
	char	*str;

	len = ft_strlen(s1) + 1;
	str = malloc(len * sizeof(char));
	if (!str)
		return (NULL);
	return (ft_memcpy(str, s1, len));
}
